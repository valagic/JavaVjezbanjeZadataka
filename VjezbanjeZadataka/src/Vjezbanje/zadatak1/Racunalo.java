package Vjezbanje.zadatak1;

public class Racunalo {

	private String bojaRacunala;
	private String modelRacunala;
	private int velicaRacunala;
	
	
	public String getBojaRacunala() {
		return bojaRacunala;
	}
	public void setBojaRacunala(String bojaRacunala) {
		this.bojaRacunala = bojaRacunala;
	}
	public String getModelRacunala() {
		return modelRacunala;
	}
	public void setModelRacunala(String modelRacunala) {
		this.modelRacunala = modelRacunala;
	}
	public int getVelicaRacunala() {
		return velicaRacunala;
	}
	public void setVelicaRacunala(int velicaRacunala) {
		this.velicaRacunala = velicaRacunala;
	}
	
	
	
}
